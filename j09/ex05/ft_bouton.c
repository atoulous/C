int		ft_bouton(int i, int j, int k)
{
	if ((j < i && i < k) || (i > k && i < j))
		return (i);
	if ((i < j && j < k) || (j > k && j < i))
		return (j);
	if ((i < k && k < j) || (k < i && k > j))
		return (k);
	else
		return (0);
}
